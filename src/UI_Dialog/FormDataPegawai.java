    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nip = jTextField1.getText().trim();
        String nama_pegawai = jTextField2.getText().trim();
        String alamat = jTextField3.getText().trim();
        String username = jTextField4.getText().trim();
        String password = jTextField5.getText().trim();
        int id_level = jComboBox1.getSelectedIndex()+1;

        String query = "INSERT INTO pegawai(nip,nama_pegawai,alamat,password,id_level) VALUES ('"+nip+"','"+nama_pegawai+"','"+alamat+"','"+username+"','"+password+"','"+id_level+"')";
        System.out.println(query);

        try {
            int i = connection.createStatement().executeUpdate(query);
            if(i==1){
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
            }else{
                JOptionPane.showMessageDialog(null, "Database error");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormDataBarang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

